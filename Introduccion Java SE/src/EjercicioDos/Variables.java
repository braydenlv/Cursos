package EjercicioDos;

public class Variables {
    public static void main(String[] args) {
        //Declara una variable (espacio de memoria)
        int speed;
        //Se asigna un valor
        speed=10;
        System.out.println(speed);

        //Se puede inicializar una variable desde que se declara
        int salary = 1000;
        String employeeName = "Brayden Lopera";
        System.out.println(employeeName + " tiene un salario de " + salary);

    }
}
